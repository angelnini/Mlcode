IMG_SIZE = (224, 224)
BATCH_SIZE = 64
EPOCHS = 100
#CUSTOM_THRESHOLD = 0.6
train_dir = '/content/my_11/11/train'
test_dir = '/content/my_11/11/test'

print("準備資料生成器")
train_val_datagen = ImageDataGenerator(
    rescale=1./255, rotation_range=10, zoom_range=0.1,
    width_shift_range=0.1, height_shift_range=0.1,
    horizontal_flip=True,
    validation_split=0.2 # Add validation_split here
)
test_datagen = ImageDataGenerator(rescale=1./255)

train_generator = train_val_datagen.flow_from_directory(
    train_dir, target_size=IMG_SIZE, batch_size=BATCH_SIZE, class_mode='binary', subset='training')
val_generator = train_val_datagen.flow_from_directory(
    train_dir, target_size=IMG_SIZE, batch_size=BATCH_SIZE, class_mode='binary', subset='validation')
test_generator = test_datagen.flow_from_directory(
    test_dir, target_size=IMG_SIZE, batch_size=BATCH_SIZE, class_mode='binary', shuffle=False)


early_stop = EarlyStopping(
    monitor='val_loss',
    patience=5,
    restore_best_weights=True,
    verbose=1
)

# 讓程式自動抓取 train_generator 裡面的標籤並計算平衡權重
class_weights = compute_class_weight(
    class_weight='balanced',
    classes=np.unique(train_generator.classes),
    y=train_generator.classes
)

# 轉換成 Keras 看得懂的字典格式 (例如 {0: 0.56, 1: 4.66})
weights_dict = dict(enumerate(class_weights))
print(f"自動計算的類別權重：{weights_dict}")

def build_lenet():
    model = Sequential([
        Conv2D(6, kernel_size=(5, 5), activation='relu', input_shape=(224, 224, 3)),
        AveragePooling2D(pool_size=(2, 2)),
        Conv2D(16, kernel_size=(5, 5), activation='relu'),
        AveragePooling2D(pool_size=(2, 2)),
        Flatten(),
        Dense(120, activation='relu'),
        Dense(84, activation='relu'),
        Dense(1, activation='sigmoid')
    ], name="LeNet")
    return model


print("正在訓練: LeNet ...")
lenet_model = build_lenet()
lenet_model.compile(optimizer=Adam(learning_rate=0.001), loss='binary_crossentropy', metrics=['accuracy'])


lenet_model.fit(
    train_generator,
    epochs=EPOCHS,
    validation_data=val_generator,
    class_weight=weights_dict,# 解決資料不平衡
    callbacks=[early_stop],
    verbose=1
)

# 評估與紀錄成績
test_generator.reset()
preds_prob = lenet_model.predict(test_generator, verbose=0)
preds_class = (preds_prob > 0.5).astype(int).flatten() # 這裡可以套用調整過的 Threshold
true_labels = test_generator.classes

final_results_dict['LeNet'] = {
    'Accuracy': accuracy_score(true_labels, preds_class),
    'Recall': recall_score(true_labels, preds_class),
    'Precision': precision_score(true_labels, preds_class),
    'F1-Score': f1_score(true_labels, preds_class)
}

lenet_model.save('lenet_model_final.h5')
print("LeNet訓練完成，模型已存檔")

