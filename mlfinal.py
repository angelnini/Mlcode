print("範例圖")

best_model = load_model('mobilenet_model_final.h5')

# 開啟shuffle=True來洗牌
demo_generator = test_datagen.flow_from_directory(
    test_dir,
    target_size=(224, 224),
    batch_size=32,
    class_mode='binary',
    shuffle=True
)

labels_map = {v: k for k, v in demo_generator.class_indices.items()}
def get_chinese_label(idx):
    name = labels_map[idx].upper()
    if 'NORM' in name: return 'Normal'
    if 'PNEU' in name: return 'Pneumonia'
    return name

selected_images = []
selected_labels = []

# 不斷抽牌，直到收集滿9張圖 (且確保包含肺炎照片)
while len(selected_images) < 9:
    images, true_labels = next(demo_generator)
    for i in range(len(images)):
        if len(selected_images) < 9:
            selected_images.append(images[i])
            selected_labels.append(true_labels[i])

selected_images = np.array(selected_images)
selected_labels = np.array(selected_labels)


preds_prob = best_model.predict(selected_images, verbose=0)
preds_class = (preds_prob > 0.5).astype(int).flatten()

plt.figure(figsize=(12, 12))

for i in range(9):
    plt.subplot(3, 3, i+1)
    plt.imshow(selected_images[i])

    true_class_name = get_chinese_label(selected_labels[i])
    pred_class_name = get_chinese_label(preds_class[i])

    if preds_class[i] == selected_labels[i]:
        color = 'green'
        icon = '[O]'
    else:
        color = 'red'
        icon = '[X]'

    title = f"{icon} result:{pred_class_name}\n ans:{true_class_name}"

    plt.title(title, color=color, fontsize=14, fontweight='bold', pad=10)
    plt.axis('off')

plt.tight_layout()
plt.show()