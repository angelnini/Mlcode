import numpy as np
from sklearn.metrics import accuracy_score, recall_score, precision_score, f1_score

def build_transfer_model(base_model_class, model_name):
    base_model = base_model_class(input_shape=(224, 224, 3), include_top=False, weights='imagenet')
    base_model.trainable = False
    x = GlobalAveragePooling2D()(base_model.output)
    x = Dense(128, activation='relu')(x)
    predictions = Dense(1, activation='sigmoid')(x)
    return Model(inputs=base_model.input, outputs=predictions, name=model_name)

print("正在訓練: MobileNetV2 ...")
mobilenet_model = build_transfer_model(MobileNetV2, "MobileNetV2")
mobilenet_model.compile(optimizer=Adam(learning_rate=0.001), loss='binary_crossentropy', metrics=['accuracy'])

history = mobilenet_model.fit(
    train_generator,
    epochs=EPOCHS,
    validation_data=val_generator,
    class_weight=weights_dict,
    callbacks=[early_stop],
    verbose=1
)

# 使用驗證集尋找最佳 Threshold (目標：最高 Accuracy)
val_generator.reset()
val_preds_prob = mobilenet_model.predict(val_generator, verbose=0)
val_true_labels = val_generator.classes

best_threshold = 0.5
best_acc = 0.0 # 變數名稱改為 best_acc

for thresh in np.arange(0.1, 0.9, 0.05):
    temp_preds_class = (val_preds_prob > thresh).astype(int).flatten()
    current_acc = accuracy_score(val_true_labels, temp_preds_class) # 改用 accuracy_score 計算

    if current_acc > best_acc:
        best_acc = current_acc
        best_threshold = thresh

print(f"找到的最佳Threshold:{best_threshold:.2f} (驗證集 Accuracy: {best_acc:.4f})")

print("\n正在評估測試集成績...")
test_generator.reset()
preds_prob = mobilenet_model.predict(test_generator, verbose=0)

# 把 Threshold 換成剛剛找到的 best_threshold
preds_class = (preds_prob > best_threshold).astype(int).flatten()
true_labels = test_generator.classes

# 將評估結果存成變數
test_acc = accuracy_score(true_labels, preds_class)
